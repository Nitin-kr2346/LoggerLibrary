namespace LoggerLibrary.Enum
{
    /// <summary>
    /// Defines the severity levels of log messages.
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Debug level for detailed troubleshooting information.
        /// </summary>
        DEBUG,

        /// <summary>
        /// Info level for detailed troubleshooting information.
        /// </summary>
        INFO,

        /// <summary>
        /// Warning level for detailed troubleshooting information.
        /// </summary>
        WARN,

        /// <summary>
        /// Error level for detailed troubleshooting information.
        /// </summary>
        ERROR,

        /// <summary>
        /// Fatal level for detailed troubleshooting information.
        /// </summary>
        FATAL
    }
}
